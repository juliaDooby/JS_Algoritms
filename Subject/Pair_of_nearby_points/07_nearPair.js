function closestToPoint(points, p) {
  let minDist = Infinity;
  let pair = [];

  for (let i = 0; i < points.length; i++) {
    const dist = distance(points[i], p);
    if (dist < minDist) {
      minDist = dist;
      pair = [points[i], p];
    }
  }

  return pair;
}

