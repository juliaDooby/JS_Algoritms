function quickSortThreeWay(arr) {
  if (arr.length <= 1) return arr;

  const pivot = arr[0];
  let left = [];
  let equal = [];
  let right = [];

  for (let num of arr) {
    if (num < pivot) {
      left.push(num);
    } else if (num > pivot) {
      right.push(num);
    } else {
      equal.push(num);
    }
  }

  return [...quickSortThreeWay(left), ...equal, ...quickSortThreeWay(right)];
}

