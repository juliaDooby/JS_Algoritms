function findPairWithSum(arr, sum) {
  arr.sort((a, b) => a - b);

  let left = 0;
  let right = arr.length - 1;

  while (left < right) {
    const currentSum = arr[left] + arr[right];
    if (currentSum === sum) {
      return [arr[left], arr[right]];
    } else if (currentSum < sum) {
      left++;
    } else {
      right--;
    }
  }

  return null;
}

